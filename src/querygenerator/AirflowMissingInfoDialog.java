    @Action
    public void selectFinalOutputFolder() {
        JFileChooser targetWorkspaceChooser = new JFileChooser();
        targetWorkspaceChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int n = targetWorkspaceChooser.showOpenDialog(null);
        String filename = targetWorkspaceChooser.getSelectedFile().toString();
        if (n == JFileChooser.APPROVE_OPTION) {
            jTextField1.setText(filename);
        }
    }

    @Action
    public void addMissingInfo() {
        
        //Air Handling System
        jTextField2.setText(airflowMissingInfo.getAhsList().get(0).getName());
        jTextField3.setText(airflowMissingInfo.getAhsList().get(0).getOutdoorAirPercent().toString());
        
        //Zones
        Map<String, Zone> zones = airflowMissingInfo.getZones();
        List<javax.swing.JPanel> zonePanels = new ArrayList<javax.swing.JPanel>();
        for(String zoneName : zones.keySet()){
            zonePanels.add(this.addZonePanel(zones.get(zoneName)));
        }
        
        //Walls
        Map<String, Wall> walls = airflowMissingInfo.getWalls();
        List<javax.swing.JPanel> wallPanels = new ArrayList<javax.swing.JPanel>();
        for(String wallName : walls.keySet()){
            wallPanels.add(this.addWallPanel(walls.get(wallName)));
        }
        this.setPanel2(zonePanels, wallPanels);
        
        jPanel1.setVisible(false);
        jScrollPane1.setVisible(true);
        jPanel4.setVisible(false);
    }
    
