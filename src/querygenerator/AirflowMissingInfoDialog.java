    private void getUpdateMissingInfo(){
        //reset ahsList in order to update data from user input
        String ahsName = jTextField2.getText();
        this.airflowMissingInfo.getAhsList().get(0).setName(ahsName);
        this.airflowMissingInfo.getAhsList().get(0).setOutdoorAirPercent(Double.valueOf(jTextField3.getText()));
        
        //reset zones in order to update data from user input
        for(String zoneName : this.airflowMissingInfo.getZones().keySet()){
            Zone zone = this.airflowMissingInfo.getZones().get(zoneName);
            SupplyAir supplyAir = zone.getSupplyAir().get(0);
            supplyAir.setSystem(ahsName);
            Flow supplyFlow = supplyAir.getFlow();
            if(zonePanelMap.get(zoneName).get(0).getText() == null || zonePanelMap.get(zoneName).get(0).getText().length() == 0){
             supplyFlow.setValue(0.0);   
            }
            else{
                supplyFlow.setValue(Double.valueOf(zonePanelMap.get(zoneName).get(0).getText()));
            }
            supplyFlow.setUnits(zonePanelMap.get(zoneName).get(1).getText());
            supplyAir.setFlow(supplyFlow);
            
            ReturnAir returnAir = zone.getReturnAir().get(0);
            returnAir.setSystem(ahsName);
            Flow returnFlow = returnAir.getFlow();
            if(zonePanelMap.get(zoneName).get(2).getText() == null || zonePanelMap.get(zoneName).get(2).getText().length() == 0){
                returnFlow.setValue(0.0);
            }
            else{
                returnFlow.setValue(Double.valueOf(zonePanelMap.get(zoneName).get(2).getText()));
            }
            returnFlow.setUnits(zonePanelMap.get(zoneName).get(3).getText());
            returnAir.setFlow(returnFlow);
            
            List<SupplyAir> supplyAirList = new ArrayList<SupplyAir>();
            supplyAirList.add(supplyAir);
            List<ReturnAir> returnAirList = new ArrayList<ReturnAir>();
            returnAirList.add(returnAir);
            
            zone.setSupplyAir(supplyAirList);
            zone.setReturnAir(returnAirList);
            
            this.airflowMissingInfo.getZones().put(zoneName, zone);     //update
        }
        
        //reset walls in order to update data from user input
        for(String wallName : this.airflowMissingInfo.getWalls().keySet()){
            Wall wall = this.airflowMissingInfo.getWalls().get(wallName);
            if(this.wallPanelMap.get(wallName).getText() == null || this.wallPanelMap.get(wallName).getText().length() == 0){
                wall.setAzimuth(0.0);
            }
            else{
                wall.setAzimuth(Double.valueOf(this.wallPanelMap.get(wallName).getText()));
            }
            this.airflowMissingInfo.getWalls().put(wallName, wall);      //update
        }
    }
    
    private void combineFinalResult(){
        Map<String,Object> map = new HashMap<String,Object>(); 
        map.put("name", airflowQueriedInfo.getName());
        map.put("description", airflowQueriedInfo.getDescription());
        
        map.put("ahs", airflowMissingInfo.getAhsList());
        map.put("levels", airflowQueriedInfo.getLevels());
        
        List<Zone> zones = new ArrayList<Zone>();
        for(String zoneName: airflowQueriedInfo.getZones().keySet()){
            Zone zone = airflowQueriedInfo.getZones().get(zoneName);
            Zone zoneMissing = airflowMissingInfo.getZones().get(zoneName);
            zone.setReturnAir(zoneMissing.getReturnAir());
            zone.setSupplyAir(zoneMissing.getSupplyAir());
            zones.add(zone);
        }
        map.put("zones", zones);
        
        List<Wall> walls = new ArrayList<Wall>();
        for(String wallName : airflowQueriedInfo.getWalls().keySet()){
            Wall wall = airflowQueriedInfo.getWalls().get(wallName);
            Wall wallMissing = airflowMissingInfo.getWalls().get(wallName);
            if(wallName.contains("Exterior")){
                wall.setAzimuth(wallMissing.getAzimuth());
            }
            walls.add(wall);
        }
        map.put("walls", walls);
        JSONObject jsonObject = JSONObject.fromObject(map);
        FileOperator fileOperator = new FileOperator();
        fileOperator.writeFile(jTextField1.getText(), this.finalFileName, jsonObject.toString());
    }
    
